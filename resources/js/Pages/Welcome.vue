<script>
export default {
    data() {
        return {
            shortenedLink: null,
            link: null,
        }
    },
    methods: {
        shorten() {
            axios.post('/api/shortener/', {
                url: this.link
            })
                .then((r) => this.shortenedLink = r.data)
                .catch((error) => alert(error.response.data.message))
        }
    }
}
</script>

<template>
    <Head title="Welcome"/>

    <div
        class="relative sm:flex sm:justify-center sm:items-center min-h-screen bg-dots-darker bg-center bg-gray-100 dark:bg-dots-lighter dark:bg-gray-900 selection:bg-red-500 selection:text-white"
    >
        <div>
            <input v-model="link" type="text"  placeholder="Ссылка для сокращения">
            <button @click="shorten" style="background-color: #2563eb; color: snow; padding: 2%">Сократить</button>
            <input :value="shortenedLink" type="text"  placeholder="Сокращенная ссылка" readonly>
        </div>
    </div>
</template>

